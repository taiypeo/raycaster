const std::vector<std::array<int, 3>> colors{
    {0, 0, 0},
    {255, 0, 0},
    {0, 255, 0},
    {0, 0, 255},
    {0, 127, 127},
    {127, 127, 0}};

int main()
{
    sf::RenderWindow window(sf::VideoMode(640, 480), "Raycaster");

    EventHandler event_handler(window);
    Camera camera(event_handler, Vector(22, 12), Vector(-1, 0), Vector(0, 0.66), 0.015, 0.003);
    World world(map, colors);
    Renderer renderer(window, camera, 100, world);

    while (window.isOpen())
    {
        sf::Event event;
        while (window.pollEvent(event))
        {
            if (event.type == sf::Event::Closed)
            {
                window.close();
            }
        }

        event_handler.handle();
        renderer.render();
    }

    return 0;
}
